// Lead magnet
function handleLeadMagnet() {
  var name  = document.getElementById('lmName').value.trim();
  var email = document.getElementById('lmEmail').value.trim();
  var re    = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  if (!name)  { document.getElementById('lmName').style.outline  = '1px solid #f1948a'; document.getElementById('lmName').focus();  return; }
  if (!email || !re.test(email)) { document.getElementById('lmEmail').style.outline = '1px solid #f1948a'; document.getElementById('lmEmail').focus(); return; }
  document.getElementById('lmName').style.outline  = 'none';
  document.getElementById('lmEmail').style.outline = 'none';
  document.querySelector('.lm-right').innerHTML = '<div style="padding:14px 20px;font-size:14px;font-weight:600;color:#7ecfea;">✓ Check your inbox — your checklist is on its way.</div>';
  document.querySelector('.lm-note').style.display = 'none';
}

// Sticky bar
var barDismissed = false;
var stickyBar    = document.getElementById('stickyBar');
var heroSection  = document.querySelector('.hero');

function checkScroll() {
  if (barDismissed) return;
  if (heroSection.getBoundingClientRect().bottom < 0) {
    stickyBar.classList.add('visible');
  } else {
    stickyBar.classList.remove('visible');
  }
}
function dismissBar() {
  barDismissed = true;
  stickyBar.classList.remove('visible');
  stickyBar.style.display = 'none';
}
window.addEventListener('scroll', checkScroll, { passive: true });

